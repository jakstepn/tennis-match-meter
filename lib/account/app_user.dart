class AppUser {
  final String name, uid, description, level, gender;
  final int age;
  final double weight, height;
  AppUser({
    required this.name,
    required this.uid,
    required this.description,
    required this.level,
    required this.gender,
    required this.age,
    required this.height,
    required this.weight,
  });
}
